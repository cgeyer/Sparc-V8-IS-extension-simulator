static void init(int* array, int size, int seed) {

	int i;
	for (i = 0; i < size; i++) {
		if (seed == SEED_WCET)
			if (i%2 == 0) { 
				array[i] = MIN_VAL - i - 1; 
			} else {
				array[i] = MAX_VAL + i + 1;
			}
		else if (seed == SEED_AVGET) {
			if (i%4 == 0) { 
				array[i] = MIN_VAL - i - 1; 
			} else if (i%4 == 1) {
				array[i] = MAX_VAL + i + 1;
			}
		} else if (seed == SEED_BCET) {
			array[i] = i;
		}
	}

}

static int threshold(int* array, int size, int min_val, int max_val) {
	int i;
	int counter = 0;
	int tmp;
	for (i = 0; i < size; i++) {
		tmp = array[i];
		if (tmp < min_val) {
			tmp = min_val;
			counter++;
		} 
		if (array[i] > max_val ) {
			tmp = max_val;
			counter++;
		}
		array[i] = tmp;
	}
	return counter;
}

int main(void) {

	int result;
	int i = 0;
	for (i = 0; i < NUM_TEST_VECTORS; i++) {

		init(&(array[i][0]), ARRAY_SIZE, seeds[i]);
		asm("sim-clearcycles");
		result = threshold(&(array[i][0]), ARRAY_SIZE, MIN_VAL, MAX_VAL);
		asm("sim-printcycles");

		if (result != results[i]) {
			return -1;
		}
	}

	return 0;
}
